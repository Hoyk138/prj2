package user.VO;

public class FindPassVO {
	
	private String id, question, answer;

	public FindPassVO(String id, String question, String answer) {
		super();
		this.id = id;
		this.question = question;
		this.answer = answer;
	}

	public String getId() {
		return id;
	}

	public String getQuestion() {
		return question;
	}

	public String getAnswer() {
		return answer;
	}

	
	

 

}
