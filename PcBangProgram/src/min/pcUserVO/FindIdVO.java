public class FindIdVO {
	
	private String name;
	private int phone;
	
	public FindIdVO(String name, int phone) {
		this.name=name;
		this.phone=phone;
		
	}

	public String getName() {
		return name;
	}

	public int getPhone() {
		return phone;
	}

	
	
}
